#include "../push_swap.h"

void	ft_pb(t_list **a, t_list **b)
{
	t_list	*aux;

	aux = (*a)-> next;
	ft_lstadd_front(b, ft_lstnewpos((*a)-> num, (*a)-> pos));
	write(1, "pb\n", 3);
	free((*a));
	(*a) = aux;
}
