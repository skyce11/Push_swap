#include "../push_swap.h"

void	ft_sb(t_list **b)
{
	t_list	*aux;

	if (!b)
		return ;
	aux = (*b);
	if ((*b)-> next)
	{
		aux = (*b)-> next;
		(*b)-> next = aux-> next;
		ft_lstadd_front(b, aux);
	}
	write(1, "sb\n", 3);
}
