#include "../push_swap.h"

void	ft_pa(t_list **a, t_list **b)
{
	t_list	*aux;

	aux = (*b)-> next;
	ft_lstadd_front(a, ft_lstnewpos((*b)-> num, (*b)-> pos));
	write(1, "pa\n", 3);
	free((*b));
	(*b) = aux;
}
